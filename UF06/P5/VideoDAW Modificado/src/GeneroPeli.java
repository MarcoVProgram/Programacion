/**
 * Enumeración que define los géneros de películas disponibles en el catálogo.
 *
 * <p>Este enum proporciona una lista cerrada de categorías de géneros cinematográficos
 * que pueden ser asignadas a objetos de tipo Película.</p>
 *
 * @author Marco
 * @version 1.0
 * @since 1.0
 */
public enum GeneroPeli {

    /** Género de películas románticas. */
    ROMANCE,

    /** Género de películas de terror. */
    TERROR,

    /** Género de películas de fantasía. */
    FANTASIA,

    /** Género de películas de comedia. */
    COMEDIA,

    /** Género de películas de acción. */
    ACCION,

    /** Categoría para géneros de películas no clasificados en las opciones anteriores. */
    OTROS
}